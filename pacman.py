def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point[0], 20) + 200) / 20
    y = (180 - floor(point[1], 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point, aim):
    """Return True if point is valid in tiles."""
    point = (point[0] + aim[0], point[1] + aim[1]) 
    index = offset(point)

    if tiles[index] == 0:
        return False


    index = offset( (point[0] + 19, point[1] + 19) )

    if tiles[index] == 0:
        return False

    return point[0] % 20 == 0 or point[1] % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    global pacman
    global ghosts
    
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman, aim):
        pacman = (pacman[0] + aim[0], pacman[1] + aim[1])
        
    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman[0] + 10, pacman[1] + 10)
    dot(20, 'yellow')

    tmp_ghosts = []
    for point, course in ghosts:
        if valid(point, course):
            point = (point[0] + course[0], point[1] + course[1])
        else:
            options = [
                (5, 0),
                (-5, 0),
                (0, 5),
                (0, -5),
            ]
            course = choice(options)

        tmp_ghosts.append((point, course))

        up()
        goto(point[0] + 10, point[1] + 10)
        dot(20, 'red')

    ghosts = tmp_ghosts

    update()

    for point, course in ghosts:
        diff = (pacman[0] - point[0], pacman[1] - point[1]) 
        dist = math.sqrt(diff[0]**2 + diff[1]**2)
        if dist < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    global aim
    if valid(pacman, (x, y)):
        aim = (x, y)


